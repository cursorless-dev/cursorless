/**
 * Describes the behavior of ranges when typing/editing at their edges.
 */
export enum RevealLineAt {
  /**
   * Reveal line at top of viewport
   */
  top = "top",

  /**
   * Reveal line at center of viewport
   */
  center = "center",

  /**
   * Reveal line at bottom of viewport
   */
  bottom = "bottom",
}
